    private void checkNumericKey(JTextField field) {
        if (field.getText().length() > 0) {
            char _typed = field.getText().charAt(field.getText().length() - 1);
            if (!(_typed >= '0' && _typed <= '9')) {
                field.setText(field.getText().substring(0, field.getText().length() - 1));
            }
        }
    }

