    /**
	 * @param evt  
	 */
    private void serverStartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_serverStartButtonActionPerformed
        try {
            int port = 0;
            try {
                port = Integer.parseInt( portField.getText() );
            } catch (NumberFormatException e) {
                portField.setText(String.valueOf(ProcessEditorServer.DEFAULT_PORT));
                port = ProcessEditorServer.DEFAULT_PORT;
            }


            if (! ProcessEditorServer.isPortInUse(port)) {
                ProcessEditorServer.startForWorkbench(httpsBox.isSelected(), port);
                serverStatus1.setText("The server is running.");
                serverStatus2.setText("URL: " + ProcessEditorServer.getInstanceForWorkbench().getUrl());

                serverStartButton.setEnabled(false);
                serverStopButton.setEnabled(true);

                this.portFieldKeyReleased(null);
            }
        } catch ( Exception ex ) {
            ex.printStackTrace();
        }

}//GEN-LAST:event_serverStartButtonActionPerformed

